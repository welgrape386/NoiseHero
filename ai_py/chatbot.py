while True:
    user_input = input("나: ")
    
    if user_input == "종료":
        print("상담을 종료합니다. 불편함이 해결되길 바랍니다.")
        break

    if not user_input.strip():
        continue
    
    # 대화 이력에 사용자 메시지 추가
    conversation_history.append({
        "role": "user",
        "content": user_input
    })
    
